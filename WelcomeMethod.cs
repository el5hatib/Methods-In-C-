using System; // System namespace       
class Methods
{
    static void SayHello() // Method to print Hello World
    {
        Console.WriteLine("Hello World!");
    }
    static void Main() // Main method
    {
        SayHello(); // Call the SayHello method         
    }
}
/*
 *************** Method *************** 
 *The method is a block of code that contains a series of statements.   
 *We have three parts of a method:  
 *(1) Method declaration: It defines the name, return type, and parameters of the method.   
 *(2) Method body: It contains the code that defines the method.        
 *(3) Method call: It is used to invoke the method.
 *syntax:
 *access-specifier return-type method-name(parameters)
